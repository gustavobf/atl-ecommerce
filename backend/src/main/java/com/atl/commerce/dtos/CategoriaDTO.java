package com.atl.commerce.dtos;

public class CategoriaDTO {

	private int id;
	private String nome;

	public CategoriaDTO() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(final int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(final String nome) {
		this.nome = nome;
	}

}
